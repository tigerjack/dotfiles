set -o nounset                              # Treat unset variables as an error
set -euo pipefail

# --- Phase 1: render templates via envsubst ---
declare -a arr=("$XDG_CONFIG_HOME/user-dirs.dirs" "$XDG_CONFIG_HOME/sway/conf.d/bar" "$XDG_CONFIG_HOME/sway/conf.d/bindings" "$XDG_CONFIG_HOME/sway/conf.d/execs" "$XDG_CONFIG_HOME/sway/conf.d/inout" "$XDG_CONFIG_HOME/sway/conf.d/wallpaper.conf" "$XDG_CONFIG_HOME/sway/conf.d/windows" "$XDG_CONFIG_HOME/sway/conf.d/azenb" "$XDG_CONFIG_HOME/sway/conf.d/battaglia");
# not needed anymore since I switched to rclone
# "$XDG_CONFIG_HOME/onedrive/atos/config" "$XDG_CONFIG_HOME/onedrive/polimi/config");

vars=$(awk 'BEGIN{for(v in ENVIRON) print "$" v}' | grep -e "XDG.*HOME" -e "MDIR_.*" | tr '\n' ' ')
echo "$vars"
for i in "${arr[@]}"; do
    if [[ ! -f "$i.template" ]]; then
        echo "ERROR: $i.template not found, skipping" >&2
        continue
    fi
    echo "Replacing vars in $i.template"
    { echo -e "#WARNING: autogenerated with bin/generate_user_dirs.sh,\n#change the .template file and rerun the script"
      envsubst "\$HOME $vars" < "$i.template"
    } > "$i"
done

# --- Phase 2: symlink per-host config variants (generate_from_global_env.sh) ---
declare -a arr=("$XDG_CONFIG_HOME/waybar/config");

# Set user directories
for i in "${arr[@]}"; do
    ln -sf "$i.$HOSTNAME" "$i"
done


